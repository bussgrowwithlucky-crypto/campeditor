# ---------------------------------------------------------------------------
# New file appearing in the library
# ---------------------------------------------------------------------------


def test_new_file_is_extracted(
    video_files: list[Path],
    index_settings: Settings,
):
    # First build with 3 files.
    log1: list[Path] = []
    build_library_index(index_settings, _extractor=_fake_extractor_factory(log1))
    assert {p.resolve() for p in log1} == {p.resolve() for p in video_files}

    # Add a new clip.
    new_clip = video_files[0].parent / "clip_d.mp4"
    new_clip.write_bytes(b"\x00" * 4096)

    # Re-build — only the new file should be extracted.
    log2: list[Path] = []
    report = build_library_index(
        index_settings, _extractor=_fake_extractor_factory(log2)
    )
    assert len(log2) == 1
    assert log2[0].resolve() == new_clip.resolve()
    assert report.indexed == 4
    assert report.re_extracted == 1


# ---------------------------------------------------------------------------
# Removed file is dropped from the cache (next rebuild sees it missing)
# ---------------------------------------------------------------------------


def test_removed_file_drops_from_cache_on_next_build(
    video_files: list[Path],
    index_settings: Settings,
):
    build_library_index(index_settings, _extractor=_fake_extractor_factory([]))
    raw_before = json.loads(index_settings.index_path.read_text(encoding="utf-8"))
    assert len(raw_before["clips"]) == 3

    video_files[0].unlink()

    build_library_index(index_settings, _extractor=_fake_extractor_factory([]))
    raw_after = json.loads(index_settings.index_path.read_text(encoding="utf-8"))
    assert len(raw_after["clips"]) == 2
    remaining = {Path(p).name for p in raw_after["clips"]}
    assert "clip_a.mp4" not in remaining