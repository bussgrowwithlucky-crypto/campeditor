"""Tests for library_indexer.build_library_index.

We exercise the indexer against a fake library directory of three tiny mp4s
(generated by conftest.make_fake_video). The extractor is monkeypatched so
the test never actually decodes a video — and so we can count extractor
calls precisely.

Coverage:
  * index.json is written with the correct schema after a first build.
  * re-running with unchanged mtime+size calls the extractor ZERO times.
  * changing mtime (touch) DOES re-extract that clip.
  * corrupt cache file is recovered gracefully (treated as empty).
  * unreadable / zero-duration clips are skipped, not added to the cache.
"""

from __future__ import annotations

import json
import os
from pathlib import Path

import pytest

from broll_intelligence import build_library_index
from broll_intelligence.config import Settings
from broll_intelligence.feature_vector import empty_feature_vector
from broll_intelligence.library_indexer import (
    INDEX_VERSION,
    VIDEO_EXTS,
    load_index,
    load_index_as_clips,
)


def _fake_extractor_factory(call_log: list[Path]):
    """Build an extractor that records every call and returns a deterministic
    FeatureVector (so cache contents are testable)."""

    def _extract(path: Path, settings: Settings):
        call_log.append(path)
        fv = empty_feature_vector(str(path), source="library")
        fv.subjects = ["fake-subject"]
        fv.category = "movie"
        fv.confidence = 0.5
        return fv

    return _extract


# ---------------------------------------------------------------------------
# First build
# ---------------------------------------------------------------------------


def test_first_build_writes_index_json(
    video_files: list[Path],
    index_settings: Settings,
):
    call_log: list[Path] = []
    report = build_library_index(index_settings, _extractor=_fake_extractor_factory(call_log))

    # All three clips indexed, all re-extracted on the first build.
    assert report.total == 3
    assert report.indexed == 3
    assert report.re_extracted == 3
    assert report.skipped == 0
    assert report.failed == 0
    assert len(call_log) == 3

    # Cache file exists + has the right top-level shape.
    cache_path = index_settings.index_path
    assert cache_path.exists()
    raw = json.loads(cache_path.read_text(encoding="utf-8"))
    assert raw["version"] == INDEX_VERSION
    assert isinstance(raw["clips"], dict)
    assert len(raw["clips"]) == 3

    # Each entry has mtime, size, features.
    for abs_path, entry in raw["clips"].items():
        assert isinstance(entry["mtime"], (int, float))
        assert isinstance(entry["size"], int)
        assert entry["mtime"] > 0
        assert entry["size"] > 0
        assert "features" in entry
        feats = entry["features"]
        assert feats["category"] == "movie"
        assert feats["subjects"] == ["fake-subject"]
        assert feats["confidence"] == 0.5
        assert Path(abs_path).resolve() == Path(entry["features"]["media_path"]).resolve() or \
               Path(abs_path).resolve() == Path(feats["media_path"]).resolve()


# ---------------------------------------------------------------------------
# Incremental: no changes → no extractor calls
# ---------------------------------------------------------------------------


def test_unchanged_files_do_not_call_extractor(
    video_files: list[Path],
    index_settings: Settings,
):
    # First build — populate the cache.
    first_log: list[Path] = []
    build_library_index(index_settings, _extractor=_fake_extractor_factory(first_log))
    assert len(first_log) == 3

    # Second build — extractor must NOT be called.
    second_log: list[Path] = []
    report = build_library_index(
        index_settings, _extractor=_fake_extractor_factory(second_log)
    )

    assert len(second_log) == 0, "extractor was called on an unchanged cache"
    assert report.indexed == 3
    assert report.re_extracted == 0
    assert report.total == 3


# ---------------------------------------------------------------------------
# Incremental: mtime change → re-extract
# ---------------------------------------------------------------------------


def test_mtime_change_re_extracts(
    video_files: list[Path],
    index_settings: Settings,
):
    first_log: list[Path] = []
    build_library_index(index_settings, _extractor=_fake_extractor_factory(first_log))
    assert len(first_log) == 3

    # Bump mtime on the first clip — only that file should re-extract.
    target = video_files[0]
    stat = target.stat()
    os.utime(target, (stat.st_atime, stat.st_mtime + 10))

    second_log: list[Path] = []
    report = build_library_index(
        index_settings, _extractor=_fake_extractor_factory(second_log)
    )

    assert len(second_log) == 1
    assert second_log[0].resolve() == target.resolve()
    assert report.re_extracted == 1
    assert report.indexed == 3  # all three still present in cache


def test_size_change_re_extracts(
    video_files: list[Path],
    index_settings: Settings,
):
    first_log: list[Path] = []
    build_library_index(index_settings, _extractor=_fake_extractor_factory(first_log))

    # Change file size WITHOUT changing mtime (same second on Windows + most
    # filesystems rounds mtime to whole seconds, so use os.utime to be safe).
    target = video_files[1]
    with target.open("ab") as fh:
        fh.write(b"\x00" * 256)
    stat = target.stat()
    os.utime(target, (stat.st_atime, stat.st_mtime + 1))

    second_log: list[Path] = []
    build_library_index(index_settings, _extractor=_fake_extractor_factory(second_log))

    assert len(second_log) == 1
    assert second_log[0].resolve() == target.resolve()


