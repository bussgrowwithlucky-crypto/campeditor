# Skip unreadable / zero-duration clips
# ---------------------------------------------------------------------------


def test_unreadable_clip_is_skipped(
    video_files: list[Path],
    index_settings: Settings,
    monkeypatch: pytest.MonkeyPatch,
):
    # Pretend one file has zero duration — probe_duration returns 0 → the
    # extractor returns an empty_feature_vector. The indexer must skip it
    # (not add it to the cache).
    from broll_intelligence import library_indexer

    def _fake_extract(path: Path, settings: Settings):
        if path.name == "clip_b.mp4":
            return empty_feature_vector(str(path), source="library")
        fv = empty_feature_vector(str(path), source="library")
        fv.subjects = ["x"]
        fv.category = "movie"
        fv.confidence = 0.5
        return fv

    report = build_library_index(index_settings, _extractor=_fake_extract)
    assert report.total == 3
    assert report.indexed == 2
    assert report.skipped == 1

    raw = json.loads(index_settings.index_path.read_text(encoding="utf-8"))
    cached_paths = [Path(p).name for p in raw["clips"]]
    assert "clip_b.mp4" not in cached_paths


def test_missing_library_dir_is_noop(
    index_settings: Settings,
):
    """If the configured library dir doesn't exist, the indexer returns an
    empty report rather than crashing."""
    # Override to a dir that doesn't exist.
    import shutil

    if index_settings.library_dir.exists():
        shutil.rmtree(index_settings.library_dir)
    assert not index_settings.library_dir.exists()

    log: list[Path] = []
    report = build_library_index(
        index_settings, _extractor=_fake_extractor_factory(log)
    )
    assert report.total == 0
    assert report.indexed == 0
    assert log == []
    # The cache file is still written (with empty clips) so downstream
    # consumers have something predictable to load.
    raw = json.loads(index_settings.index_path.read_text(encoding="utf-8"))
    assert raw["clips"] == {}


